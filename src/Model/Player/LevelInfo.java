package Model.Player;

public class LevelInfo {
    private String id;
    private int unitALevel;
    private int unitBLevel;
    private int unitCLevel;
    private int unitDLevel;
    private int unitELevel;
    private int unitFLevel;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getUnitALevel() {
        return unitALevel;
    }

    public void setUnitALevel(int unitALevel) {
        this.unitALevel = unitALevel;
    }

    public int getUnitBLevel() {
        return unitBLevel;
    }

    public void setUnitBLevel(int unitBLevel) {
        this.unitBLevel = unitBLevel;
    }

    public int getUnitCLevel() {
        return unitCLevel;
    }

    public void setUnitCLevel(int unitCLevel) {
        this.unitCLevel = unitCLevel;
    }

    public int getUnitDLevel() {
        return unitDLevel;
    }

    public void setUnitDLevel(int unitDLevel) {
        this.unitDLevel = unitDLevel;
    }

    public int getUnitELevel() {
        return unitELevel;
    }

    public void setUnitELevel(int unitELevel) {
        this.unitELevel = unitELevel;
    }

    public int getUnitFLevel() {
        return unitFLevel;
    }

    public void setUnitFLevel(int unitFLevel) {
        this.unitFLevel = unitFLevel;
    }
}
